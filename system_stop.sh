#!/bin/bash
echo "========================================================="
echo "🛑 프로젝트 종료 및 조교님 PC 환경 원상복구를 시작합니다."
echo "========================================================="

# 1. 백그라운드에서 돌던 웹 서버 프로세스 종료
echo "🧹 실행 중인 웹 서버 프로세스(Next.js, Uvicorn) 종료 중..."
pkill -f "next-dev"
pkill -f "uvicorn"

# 2. 가동 중인 도커 컨테이너 중지 및 완전 삭제
echo "🗑️  가동 중인 Docker DB 컨테이너 정리 중..."
sudo docker stop mysql > /dev/null 2>&1
sudo docker rm mysql > /dev/null 2>&1

# 3. 다운로드받았던 기가바이트 단위의 MySQL 이미지 자체를 파기 (흔적 제거 핵심)
echo "🔥 다운로드했던 Docker MySQL 이미지 완전히 삭제 중..."
sudo docker rmi mysql:8.0 > /dev/null 2>&1

echo "========================================================="
echo "✅ 모든 서버가 안전하게 종료되었습니다."
echo "✨ 조교님 PC 환경이 100% 깨끗하게 원상복구 되었습니다. 감사합니다!"
echo "========================================================="